#OBJETIVO: Receba 2 números inteiro e realize a soma 
# entre eles, no final mostre o resultado de forma
# personalizada.

# Trabalhando com INPUT


### ENTRADA ############
# Recebendo o primeiro numero a uma variavel
valor1 = int(input("Digite o valor 1: "))

# Recebendo o segundo numero a uma variavel
valor2 = int(input("Digite o valor 2: "))


### PROCESSAMENTO ############
# Realizando a soma entre as duas variaveis
resultado = valor1 + valor2

### SAÍDA ##############
# Mostrando o resultado personalizado
print("O resultado da soma de ", valor1, "+", valor2, "é igual = ", resultado)
